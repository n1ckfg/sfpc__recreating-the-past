//--------------------------------------------------------------
void ofApp::draw(){
    ofBackground(0);
    
    ofPoint a(50, 50);
    ofPoint b(400, 400);
    ofPoint c(200, 80);
    ofPoint d(mouseX, mouseY);
    
    ofDrawLine(a, b);
    ofDrawLine(c, d);
    
    ofPoint intersection;
    if (ofLineSegmentIntersection(a, b, c, d, intersection)) {
        ofDrawCircle(intersection, 4);
    }
}

